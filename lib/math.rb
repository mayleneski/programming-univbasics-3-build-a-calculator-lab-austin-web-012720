def addition(num1, num2)
  puts result = num2 + num1
  result
end

def subtraction(num1, num2)
  puts result = num1 - num2
  result
end

def division(num1, num2)
  puts result = num1 / num2
  result
end

def multiplication(num1, num2)
  puts result = num1 * num2
  result
end

def modulo(num1, num2)
  puts result = num2 % num1
  result
end

def square_root(num)
  puts result = Math.sqrt(num)
  result
end
